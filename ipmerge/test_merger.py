    def test_deskmerge(self):
        networks = [
            ip_network('10.0.0.0/29'),
            ip_network('10.0.0.8/31'),
            ip_network('10.0.0.10/31'),
            ip_network('10.0.0.12/30'),
            ip_network('10.0.0.16/29'),
            ip_network('10.0.0.24/29'),
            ip_network('10.0.0.32/28'),
            ip_network('10.0.0.48/28'),
        ]
        expected = [
            ip_network('10.0.0.0/26'),
        ]
        result = merge(networks)
        assert result == expected

